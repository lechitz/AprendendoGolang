package main

import "fmt"

func main() {
	n1 := 10
	n2 := 50
	soma := n1 + n2
	fmt.Println("Soma:", soma)

	subtracao := n1 - n2
	fmt.Println("Subtração:", subtracao)

	divisao := n1 / n2
	fmt.Println("Divisão:", divisao)

	multiplicacao := n1 * n2
	fmt.Println("Multiplicação:", multiplicacao)

	resto := n1 % n2
	fmt.Println("Resto:", resto)
}
